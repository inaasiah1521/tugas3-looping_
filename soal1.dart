void main() {
  int i = 2; // Inisialisasi nilai i untuk looping pertama
  int j = 20; // Inisialisasi nilai j untuk looping kedua

  print("LOOPING PERTAMA");
  while (i <= 20) {
    print("$i - I love coding");
    i += 2; // Melakukan penambahan 2 pada setiap iterasi
  }

  print("LOOPING KEDUA");
  while (j >= 2) {
    print("$j - I will become a mobile developer");
    j -= 2; // Melakukan pengurangan 2 pada setiap iterasi
  }
}
